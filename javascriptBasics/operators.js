// ASSIGNMENT OPERATOR - used to assign values using initialization (right hand side of the operator) to supply the declaration (left side of the operator) a value: =
let x = 5;

//COMPARISON OPERATORS

// EQUAL TO:
'3' == 3;
console.log('3' == 3);
// JS is helpful and uses coercion when comparing values
// Coercion converts a value from one type to another.

// STRICT EQUAL TO:
// Compares the datatype AND value of the values it's checking
// Strict equal is three equal signs
console.log(3 === 3);
console.log(3 === '3');

// NOT EQUAL TO
console.log('4' != 4);

// STRICT NOT EQUAL TO:
// Compares the datatype of the two values to check if they are not equal.
console.log('4' !== 4);

// GREATER THAN:
3 > 2;

// LESS THAN:
2 < 3;

// GREATER THAN OR EQUAL TO
3 >= 2;

// LESS THAN OR EQUAL TO
2 <= 3;

// AND OPERATOR
2 && 3;

// OR OPERATOR
2 || 3;

// LOGIC OPERATORS
// Addition Operator +=
let r = 2; // added this so that the powershell doesn't throw errors
r += 1; // r = r + 1;

// Subtraction Operator
r -= 1; // r = r - 1

// Multiplication Operator *=
r *= 1; // r = r * 1

// Division Operator /=
r /= 1; //r = r / 1

// Remainder Operator (Modulus) %=
r %= 1; // r = r % 1;

// Expoenential Operator **=
r **= 1; // r = r ** 1;